// Draw OLED
bool oled_task_user() {
    if (is_keyboard_master()) {
        switch (get_highest_layer(layer_state)) {
            case DEFAULT:
                render_animation_layer0();
                break;
            case NUMBER:
                render_animation_layer1();
                break;
            case SYMBOL:
                render_animation_layer2();
                break;
            case SETTING:
                render_animation_layer3();
                break;
        }
    } else {
        render_static_animation();
    }

    return false;
}

